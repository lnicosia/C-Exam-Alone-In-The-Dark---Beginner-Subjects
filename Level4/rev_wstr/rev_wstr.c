#include <unistd.h>

int		main(int ac, char **av)
{
	int	i;
	int	j;

	i = 0;
	if (ac == 2)
	{
		while (av[1][i])
			i++;
		i--;
		while (i > 0)
		{
			j = 1;
			while (av[1][i] != ' ' && i >= 0)
				i--;
			while (av[1][i + j] != ' ' && av[1][i + j])
			{
				write(1, &(av[1][i + j]), 1);
				j++;
			}
			if (i > 0)
				write(1, " ", 1);
			i--;
		}
	}
	write(1, "\n", 1);
	return(0);
}
